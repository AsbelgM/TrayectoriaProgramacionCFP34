class Calculadora:
    def sumar(self, numeroIngresadoUno, numeroIngresadoDos):
        sumar = numeroIngresadoUno + numeroIngresadoDos
        return sumar

    def dividir(self, numeroIngresadoUno, numeroIngresadoDos):
        dividir = numeroIngresadoUno / numeroIngresadoDos
        return dividir

    def restar(self, numeroIngresadoUno, numeroIngresadoDos):
        restar = numeroIngresadoUno - numeroIngresadoDos
        return restar

    def multiplicar(self, numeroIngresadoUno, numeroIngresadoDos):
        multiplicar = numeroIngresadoUno * numeroIngresadoDos
        return multiplicar
